def performance(array: list[list[int]]) -> int:
    """Zählt die Kanten innerhalb eines Gitters."""
    edges = 0
    if (len(array) < 2 or len(array[0]) < 2):
        raise AssertionError("Array must have at least dimensions [2, 2]")
    print("Read array with dimensions", len(array), len(array[0]))
    for x in range(len(array) - 1):
        for y in range(len(array[0]) - 1):
            if (array[x][y] != array[x][y+1]):
                edges += 1
            if (array[x][y] != array[x+1][y]):
                edges += 1
    for x in range(len(array) - 1):
        if (array[x][y] != array[x+1][y]):
            edges += 1
    for y in range(len(array[0]) - 1):
        if (array[x][y] != array[x][y+1]):
            edges += 1
    return edges


